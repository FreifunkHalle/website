	<h2>Was will Freifunk?</h2>
    	<p>	
		Wir wollen gemeinsam ein freies, offenes, nicht-kommerzielles und unzensiertes Funknetzwerk mit WLAN-Technologie aufbauen. 
	</p>

	<h2>Was kann Freifunk?</h2>
    	<p>	
		Mit Freifunk kann man eigene Dienste und einen WLan Zugang zum Freifunknetz und zum Internet bereitstellen.
	</p>	

    	<h2>Internet teilen? Ja!</h2>
    	<p>	
		Wer einen Internetzugang hat, kann diesen mit einem sicheren VPN-Tunnel 
		im Freifunknetz mit anderen Nutzern teilen.
	</p>

    	<h2>VPN-Tunnel</h2>
    	<p>	
		Die VPN-Tunnel verbinden Netzknoten verschlüsselt über das Internet. 
		Schreib bitte im Forum eine Nachricht, 
		wenn du Dateien für VPN-Tunnel benötigst.
	</p>

    	<h2>anonym ins Internet</h2>
    	<p>	
		Mit dieser Infrastruktur bieten wir anonyme Internetnutzung an.
	</p>

	<h2>Sicherheit</h2>
	<p>	Die weltweite, automatisierte, verdachtsunabhängigen Massenüberwachung der elektronischen Sprach- und 
		Datenkommunikation durch die Geheimdienste gefährdet ein freies und offenes Internet. 
		Das Freifunknetz ist ein dezentrales Funk-Netz. Dezentrale Netze sind strukturbedingt schwerer zu überwachen. 
	</p>
    	<h2>Hilfe</h2>
    	<p>	
		Hilfe gibt es im Forum. Technische Dokumentation findest du im Wiki. 
		Andere Freifunker triffst du beim Stammtisch.
	</p>

